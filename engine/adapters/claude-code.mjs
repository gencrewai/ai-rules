/**
 * claude-code.mjs — Claude Code CLAUDE.md generation adapter
 */

/**
 * @param {string[]} blocks - Array of assembled markdown blocks
 * @param {object} toolConfig - tools.claude-code config from profiles/*.yaml
 * @param {object} profile - Full profile object
 * @returns {{ path: string, content: string }[]}
 */
export function generate(blocks, toolConfig, profile) {
  const timestamp = new Date().toISOString()
  const isGlobal = profile.project === 'global'
  const isInstructions = toolConfig.output.endsWith('instructions.md')
  const title = isInstructions
    ? `# AI Rules — Common Rules (auto-generated)`
    : isGlobal
      ? `# CLAUDE.md — Global Rules (Global Rules (all projects))`
      : `# CLAUDE.md — ${profile.project} Project Rules`
  const note = isInstructions
    ? `> For project-specific rules, see CLAUDE.md. This file is managed by ai-rules.`
    : isGlobal
      ? `> Common rules. For project-specific rules, see each project's CLAUDE.md.`
      : `> Project-specific rules. For common rules, see ~/.claude/CLAUDE.md.\n> **When rules in this file conflict with global core, global core (security/git/workflow) takes precedence.**`

  const header = [
    title,
    ``,
    `> ⚠️ Generated by [ai-rules](https://github.com/gencrewai/ai-rules). Edit in \`ai-rules/core/\` or \`ai-rules/extensions/\`, not here.`,
    `> Last sync: ${timestamp}`,
    `> Profile: ${profile.project}`,
    note,
    ``,
  ].join('\n')

  const files = []

  // Main CLAUDE.md
  files.push({
    path: toolConfig.output,
    content: header + blocks.join('\n\n---\n\n'),
  })

  // sub_rules: backend/CLAUDE.md, src/CLAUDE.md etc.
  if (toolConfig.sub_rules) {
    for (const sub of toolConfig.sub_rules) {
      const subBlocks = resolveExtensionBlocks(sub.extensions)
      const subHeader = [
        `# ${sub.path} — Domain Rules`,
        ``,
        `> ⚠️ Generated by ai-rules. Edit in \`ai-rules/extensions/\`.`,
        `> Last sync: ${timestamp}`,
        ``,
      ].join('\n')
      files.push({
        path: sub.path,
        content: subHeader + subBlocks.join('\n\n---\n\n'),
      })
    }
  }

  return files
}

/**
 * Emit one `.claude/agents/{name}.md` per agent.
 * Claude Code is the native target, so we keep frontmatter + body verbatim.
 *
 * @param {Array<{ name: string, content: string }>} agents
 * @param {object} toolConfig - tools['claude-code'] config
 * @param {object} profile
 * @returns {{ path: string, content: string }[]}
 */
export function generateAgents(agents, toolConfig, _profile) {
  if (!agents?.length) return []
  if (toolConfig?.agents?.enabled === false) return []
  const outputDir = toolConfig?.agents?.output || '.claude/agents/'
  return agents.map(a => ({
    path: `${outputDir.replace(/\/?$/, '/')}${a.name}.md`,
    content: a.content,
  }))
}

// Helper to directly read extension block content (injected from sync.mjs)
let _extensionBlocks = {}
export function setExtensionBlocks(blocks) {
  _extensionBlocks = blocks
}
function resolveExtensionBlocks(names) {
  return names.map(name => _extensionBlocks[name] || `<!-- extension '${name}' not found -->`).filter(Boolean)
}
