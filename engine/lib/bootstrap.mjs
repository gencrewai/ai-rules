// ── .env.example ─────────────────────────────────────────────────────────────

function generateEnvExample(name, preset) {
  const dbName = name.replace(/-/g, '_')
  const lines = [
    '# .env.example — Copy to .env and fill in real values',
    `# Project: ${name}`,
    '',
  ]

  // Database
  if (preset.database === 'postgres') {
    lines.push('# Database')
    lines.push(`DATABASE_URL=postgresql://postgres:postgres@localhost:5432/${dbName}`)
    lines.push('')
  } else if (preset.database === 'mysql') {
    lines.push('# Database')
    lines.push(`DATABASE_URL=mysql://root:password@localhost:3306/${dbName}`)
    lines.push('')
  } else if (preset.database === 'mongodb') {
    lines.push('# Database')
    lines.push(`MONGODB_URL=mongodb://localhost:27017/${dbName}`)
    lines.push('')
  }

  // Backend auth
  if (preset.backend === 'fastapi') {
    lines.push('# Auth (FastAPI)')
    lines.push('SECRET_KEY=your-secret-key-minimum-32-characters')
    lines.push('ALGORITHM=HS256')
    lines.push('ACCESS_TOKEN_EXPIRE_MINUTES=15')
    lines.push('REFRESH_TOKEN_EXPIRE_DAYS=7')
    lines.push('')
  } else if (preset.backend === 'express') {
    lines.push('# Auth (Express)')
    lines.push('JWT_SECRET=your-secret-key-minimum-32-characters')
    lines.push('JWT_EXPIRES_IN=15m')
    lines.push('')
  }

  // Frontend
  if (preset.frontend !== 'none') {
    const prefix = preset.frontend === 'next' ? 'NEXT_PUBLIC' : 'VITE'
    lines.push('# Frontend')
    lines.push(`${prefix}_API_URL=http://localhost:8000/api/v1`)
    lines.push('')
  }

  lines.push('# Payment (if needed)')
  lines.push('# STRIPE_SECRET_KEY=sk_test_...')
  lines.push('# STRIPE_WEBHOOK_SECRET=whsec_...')
  lines.push('')
  lines.push('# Production')
  lines.push('# ALLOWED_ORIGINS=https://myapp.com')
  lines.push('# SENTRY_DSN=')

  return lines.join('\n') + '\n'
}

