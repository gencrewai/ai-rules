// ── .claude/settings.local.json ──────────────────────────────────────────────

function generateSettingsLocal(preset) {
  const allow = [
    // Read tools
    'Read', 'Edit', 'Write',
    // Git (safe subset)
    'Bash(git status*)', 'Bash(git log*)', 'Bash(git diff*)',
    'Bash(git fetch*)', 'Bash(git branch*)', 'Bash(git checkout -b *)',
    'Bash(git add*)', 'Bash(git commit*)', 'Bash(git push origin *)',
    'Bash(git stash*)', 'Bash(git rev-list*)',
    // Node/NPM
    'Bash(node *)', 'Bash(npm *)', 'Bash(npx *)',
    // Utilities
    'Bash(ls *)', 'Bash(cat *)', 'Bash(head *)', 'Bash(tail *)',
    'Bash(find *)', 'Bash(grep *)', 'Bash(echo *)', 'Bash(cp *)',
    'Bash(mkdir *)', 'Bash(tree *)', 'Bash(wc *)', 'Bash(curl *)',
    // GitHub CLI
    'Bash(gh *)',
    // Web
    'WebFetch(domain:*)',
  ]

  // Stack-specific permissions
  if (preset.backend === 'fastapi') {
    allow.push('Bash(python *)', 'Bash(pip *)', 'Bash(ruff *)', 'Bash(alembic *)')
  } else if (preset.backend === 'express') {
    allow.push('Bash(tsc *)')
  } else if (preset.backend === 'django') {
    allow.push('Bash(python *)', 'Bash(pip *)')
  }

  if (preset.database === 'postgres' || preset.database === 'mysql') {
    allow.push('Bash(docker *)', 'Bash(docker-compose *)')
  }

  return { permissions: { allow } }
}

// ── Agent files ──────────────────────────────────────────────────────────────

function copyAgents(projectDir, aiRulesRoot) {
  const log = []
  const srcDir = join(aiRulesRoot, 'core', 'agents')
  const dstDir = join(projectDir, '.claude', 'agents')
  mkdirSync(dstDir, { recursive: true })

  const files = readdirSync(srcDir).filter(f => f.endsWith('.md'))
  for (const f of files) {
    copyFileSync(join(srcDir, f), join(dstDir, f))
    log.push(`  COPY: .claude/agents/${f}`)
  }
  return log
}

// ── docs/00_INDEX.md ─────────────────────────────────────────────────────────

function generateDocsIndex(name, preset) {
  const frontendLabel = STACKS.frontend[preset.frontend]?.label ?? preset.frontend
  const backendLabel = STACKS.backend[preset.backend]?.label ?? preset.backend
  const dbLabel = STACKS.database[preset.database]?.label ?? preset.database

  return `# docs/00_INDEX.md — ${name}

> Generated: ${todayISO()} | Stack: ${frontendLabel} + ${backendLabel} + ${dbLabel}

## Document Priority

\`\`\`
1. INTENT.md          — Current task goal (highest priority)
2. CLAUDE.md          — AI behavior rules
3. docs/00_INDEX.md   — This file (document map)
4. Role-specific docs — Reference
5. Existing code      — Legacy / conventions
\`\`\`

## Session Management

- **Session start**: Check SESSION.md HANDOFF block -> Check INTENT.md -> git status
- **Session end**: Update SESSION.md HANDOFF block (mandatory)
- **Context limit** (~30 turns): Snapshot to SESSION.md, request new session
`
}

// ── Utilities ────────────────────────────────────────────────────────────────

function todayISO() {
  return new Date().toISOString().split('T')[0]
}

function writeFile(projectDir, relativePath, content) {
  const fullPath = join(projectDir, relativePath)
  const dir = join(projectDir, ...relativePath.split('/').slice(0, -1))
  if (dir !== projectDir) mkdirSync(dir, { recursive: true })
  writeFileSync(fullPath, content, 'utf-8')
  return `  CREATE: ${relativePath}`
}
