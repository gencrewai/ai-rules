/**
 * bootstrap — Self-contained project initialization
 *
 * Replaces the external starter-kit's init-project.mjs.
 * Generates project structure using ai-rules core/ assets directly.
 * Zero external dependencies — Node.js built-in modules only.
 */

import {
  copyFileSync, existsSync, mkdirSync,
  readFileSync, readdirSync, writeFileSync,
} from 'fs'
import { join } from 'path'

// ── Stack Presets ───────────────────────────────────────────────────────────

const STACKS = {
  frontend: {
    react:  { label: 'React + TypeScript + Vite' },
    next:   { label: 'Next.js 14+ (App Router)' },
    vue:    { label: 'Vue 3 + TypeScript + Vite' },
  },
  backend: {
    fastapi: { label: 'FastAPI + Python 3.11' },
    express: { label: 'Express + TypeScript' },
    django:  { label: 'Django + DRF' },
    none:    { label: 'None (Frontend only)' },
  },
  database: {
    postgres: { label: 'PostgreSQL 15+' },
    mysql:    { label: 'MySQL 8.0+' },
    mongodb:  { label: 'MongoDB' },
    none:     { label: 'None' },
  },
}

const PRESETS = {
  'react-fastapi-postgres': { frontend: 'react', backend: 'fastapi', database: 'postgres' },
  'next-fastapi-postgres':  { frontend: 'next',  backend: 'fastapi', database: 'postgres' },
  'react-express-postgres': { frontend: 'react', backend: 'express', database: 'postgres' },
  'react-express-mongodb':  { frontend: 'react', backend: 'express', database: 'mongodb' },
  'next-none-none':         { frontend: 'next',  backend: 'none',    database: 'none' },
}

// ── Main Entry ──────────────────────────────────────────────────────────────

/**
 * @param {object} options
 * @param {string} options.projectDir  Absolute path to the new project directory
 * @param {string} options.name        Project name (kebab-case)
 * @param {string} options.stack       Preset stack key (e.g. 'react-fastapi-postgres')
 * @param {string} options.aiRulesRoot Absolute path to the ai-rules repository root
 * @returns {string[]} log messages
 */
export async function bootstrapProject({ projectDir, name, stack, aiRulesRoot }) {
  const log = []
  const preset = PRESETS[stack]
  if (!preset) {
    throw new Error(
      `Unknown stack preset: "${stack}". Available: ${Object.keys(PRESETS).join(', ')}`
    )
  }

  // Create project root
  mkdirSync(projectDir, { recursive: true })

  // 1. CLAUDE.md — compose from core/rules/*.md
  log.push(writeClaudeMd(projectDir, name, preset, aiRulesRoot))

  // 2. INTENT.md
  log.push(writeFile(projectDir, 'INTENT.md', generateIntentMd(name)))

  // 3. SESSION.md
  log.push(writeFile(projectDir, 'SESSION.md', generateSessionMd(name)))

  // 4. FAILURE_LOG.md
  log.push(writeFile(projectDir, 'FAILURE_LOG.md', generateFailureLogMd(name)))

  // 5. .env.example
  log.push(writeFile(projectDir, '.env.example', generateEnvExample(name, preset)))

  // 6. .gitignore
  log.push(writeFile(projectDir, '.gitignore', generateGitignore(preset)))

  // 7. .claude/settings.local.json
  const settingsDir = join(projectDir, '.claude')
  mkdirSync(settingsDir, { recursive: true })
  log.push(writeFile(
    projectDir,
    '.claude/settings.local.json',
    JSON.stringify(generateSettingsLocal(preset), null, 2) + '\n',
  ))

  // 8. .claude/agents/*.md — copy from core/agents/
  log.push(...copyAgents(projectDir, aiRulesRoot))

  // 9. docs/00_INDEX.md
  mkdirSync(join(projectDir, 'docs'), { recursive: true })
  log.push(writeFile(projectDir, 'docs/00_INDEX.md', generateDocsIndex(name, preset)))

  log.push(`\n  Project bootstrapped: ${projectDir}`)
  return log
}

// ── CLAUDE.md ────────────────────────────────────────────────────────────────

function writeClaudeMd(projectDir, name, preset, aiRulesRoot) {
  const rulesDir = join(aiRulesRoot, 'core', 'rules')
  const ruleFiles = readdirSync(rulesDir)
    .filter(f => f.endsWith('.md'))
    .sort()

  const frontendLabel = STACKS.frontend[preset.frontend]?.label ?? preset.frontend
  const backendLabel = STACKS.backend[preset.backend]?.label ?? preset.backend
  const dbLabel = STACKS.database[preset.database]?.label ?? preset.database

  const header = [
    `# CLAUDE.md — ${name}`,
    '',
    `> Generated by [ai-rules](https://github.com/gencrewai/ai-rules) scaffold`,
    `> Date: ${todayISO()}`,
    `> Stack: ${frontendLabel} + ${backendLabel} + ${dbLabel}`,
    '',
    '<!-- ai-rules:keep — Custom rules below this line are preserved on re-sync -->',
    '',
  ].join('\n')

  const sections = ruleFiles.map(f => {
    const content = readFileSync(join(rulesDir, f), 'utf-8')
    return content.trim()
  })

  const fullContent = header + sections.join('\n\n---\n\n') + '\n'
  writeFileSync(join(projectDir, 'CLAUDE.md'), fullContent, 'utf-8')
  return '  CREATE: CLAUDE.md (composed from core/rules/)'
}

